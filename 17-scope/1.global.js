// Global Scope:
// The global scope in JavaScript refers to the outermost scope where variables 
// and functions are accessible from anywhere within the codebase.
// Variables and functions declared in the global scope are accessible 
// throughout the entire JavaScript program.

Example:

// Variables declared in the global scope
var globalVar = "I am a global variable";

function globalFunction() {
console.log("I am a global function");
}

console.log(globalVar); // Output: I am a global variable
globalFunction(); // Output: I am a global function
