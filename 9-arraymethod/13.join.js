// join(): Joins all elements of an array into a string.

let fruits = ["Mango", "Orange", "Banana", "Kiwi"]

let fruitsString = fruits.join(", ");
console.log(fruitsString); // Output: "Mango, Orange, Banana, Kiwi"

let anotherString = fruits.join("% ");
console.log(anotherString); // Output: "Mango% Orange% Banana% Kiwi"
