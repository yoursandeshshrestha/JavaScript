// ! Logical Operators:

  // ?  AND (&&): Returns true if both operands are true.
    Example: 
    (5 > 3) && (4 < 7) // returns true.
    
  // ?  OR (||): Returns true if either operand is true.
    Example: 
    (5 < 3) || (4 < 7) // returns true.
    
  // ? NOT (!): Returns the opposite of the operand.
    Example: 
    !(5 == 3) // returns true.